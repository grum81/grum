CREATE TABLE contacts (
    id INT PRIMARY KEY auto_increment,
    first_name NVARCHAR,
    last_name NVARCHAR,
    company NVARCHAR,
    business_unit NVARCHAR,
    department NVARCHAR,
    team NVARCHAR,
    email_address NVARCHAR,
    external_telephone_no NVARCHAR,
    internal_telephone_no NVARCHAR,
    mobile_no NVARCHAR    
);